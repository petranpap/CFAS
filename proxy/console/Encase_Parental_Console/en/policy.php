<p lang="en-US" style="margin-bottom: 0in; line-height: 100%"><br/>

</p>
<p style="margin-bottom: 0in; line-height: 100%"><font face="Calibri, serif">Social
web platforms have enabled enriched forms of communication and
collaboration between users. As a result they have attracted an
immense user base which exploits their advantages to both share
content of various forms (news, videos, posts, bookmarks, etc.) and
to interact in an evolving manner. However, the popularity of these
platforms has rendered them targets of unscrupulous activity, such as
spam, malware, cyberbullying and sexual predation. This makes users,
and especially minors, vulnerable to data loss, fraud, abuse, and
privacy violations.</font></p>
<p style="margin-bottom: 0in; line-height: 100%"><br/>

</p>
<p style="margin-bottom: 0in; line-height: 100%"><span lang="en-US">ENCASE
will leverage the latest advances in usable security and privacy to
design and implement a browser-based web architecture for the
protection of minors from malicious actors in online social networks.
The ENCASE user-centric architecture will consist of three distinct
services, which can be combined to form an effective protective net
against cyberbullying and sexually abusive acts: </span>
</p>
<p lang="en-US" style="margin-bottom: 0in; line-height: 100%"><br/>

</p>
<p style="margin-bottom: 0in; line-height: 100%"><span lang="en-US">a)
a browser add-on with its corresponding scalable back-end
infrastructure and algorithmic techniques that collects the users’
online actions and communications to unveil incidents of aggressive
or distressed behavior; </span>
</p>
<p lang="en-US" style="margin-bottom: 0in; line-height: 100%"><br/>

</p>
<p style="margin-bottom: 0in; line-height: 100%"><span lang="en-US">b)
a browser add-on with its associated scalable software stack that
analyses social web data to detect fraudulent and fake activity and
alert the user; and </span>
</p>
<p lang="en-US" style="margin-bottom: 0in; line-height: 100%"><br/>

</p>
<p style="margin-bottom: 0in; line-height: 100%"><span lang="en-US">c)
a browser add-on that detects when a user is about to share sensitive
content (e.g., photos or address information) with an inappropriate
audience and warns the user or his parents of the imminent privacy
threat. Moreover, the last add-on has usable controls that enable
users to protect their content by suggesting suitable access lists,
by watermarking, and by securing the content via cryptography or
steganography. The three add-ons and the produced social web data
analytics software stack will be assessed with user studies and
piloting activities and will be released to the public. </span>
</p>
